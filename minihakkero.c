void *utils_func_pcol(unsigned short int msg_type){


    char colorstr[16];
    memset(colorstr,0,16);

    switch (msg_type) {
    case 0:
        strcpy(colorstr,"\e[01;32m");
        break;
    case 1:
        strcpy(colorstr,"\e[01;33m");
        break;
    case 2:
        strcpy(colorstr,"\e[01;31m");
        break;
    case 3:
        strcpy(colorstr,"\e[01;35m");
        break;
    case 10:
        strcpy(colorstr,"\e[01;36m");
        break;
    default:
        return NULL;
        break;

    }
    fprintf(stderr,"%s",colorstr);

}

